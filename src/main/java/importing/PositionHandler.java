package importing;

public class PositionHandler {
    private int foreNameColumnPos = 0;
    private int nameColumnPos = 0;
    private int numberOfBooksPos = 0;
    private int pointsPos = 0;
    private int percentagePos = 0;
    private int userNamePos = 0;
    private int beginIndex = 0;

    public int getForeNameColumnPos() {
        return foreNameColumnPos;
    }

    public void setForeNameColumnPos(int foreNameColumnPos) {
        this.foreNameColumnPos = foreNameColumnPos;
    }

    public int getNameColumnPos() {
        return nameColumnPos;
    }

    public void setNameColumnPos(int nameColumnPos) {
        this.nameColumnPos = nameColumnPos;
    }

    public int getNumberOfBooksPos() {
        return numberOfBooksPos;
    }

    public void setNumberOfBooksPos(int numberOfBooksPos) {
        this.numberOfBooksPos = numberOfBooksPos;
    }

    public int getPointsPos() {
        return pointsPos;
    }

    public void setPointsPos(int pointsPos) {
        this.pointsPos = pointsPos;
    }

    public int getPercentagePos() {
        return percentagePos;
    }

    public void setPercentagePos(int percentagePos) {
        this.percentagePos = percentagePos;
    }

    public int getUserNamePos() {
        return userNamePos;
    }

    public void setUserNamePos(int userNamePos) {
        this.userNamePos = userNamePos;
    }

    public int getBeginIndex() {
        return beginIndex;
    }

    public void setBeginIndex(int beginIndex) {
        this.beginIndex = beginIndex;
    }
}
